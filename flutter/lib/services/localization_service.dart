  static final Map<String, String> _languageNames = {
    'en': 'English',
    'fr': 'Français',
    'de': 'Deutsch',
    'ru': 'Русский',
    'ar': 'العربية',
    'it': 'Italiano',
    'he': 'עברית',
  };

  static const Set<String> _rtlLanguages = {'ar', 'he'};

  List<String> get supportedLanguageCodes =>
      _localizedValues.keys.toList(growable: false);

  List<Locale> get supportedLocales => supportedLanguageCodes
      .map((code) => code.contains('-')
          ? Locale(code.split('-').first, code.split('-')[1])
          : Locale(code))
      .toList(growable: false);

  String get currentLanguageCode => _currentLanguageCode;

  String get currentLanguageName => languageName(_currentLanguageCode);

  TextDirection get textDirection =>
      _rtlLanguages.contains(_currentLanguageCode)
          ? TextDirection.rtl
          : TextDirection.ltr;

  String languageName(String code) => _languageNames[code] ?? code;

  String t(String key) {
    return _localizedValues[_currentLanguageCode]?[key] ??
        _localizedValues['en']?[key] ??
        key;
  }

  String format(String key, [Map<String, String>? params]) {
    var value = t(key);
    if (params == null || params.isEmpty) {
      return value;
    }
    params.forEach((placeholder, replacement) {
      value = value.replaceAll('{$placeholder}', replacement);
    });
    return value;
  }

  Future<void> setLanguage(String code) async {
    if (!_localizedValues.containsKey(code) || code == _currentLanguageCode) {
      return;
    }
    _currentLanguageCode = code;
    notifyListeners();
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_prefsKey, code);
  }

  Future<void> _loadSavedLanguage() async {
    final prefs = await SharedPreferences.getInstance();
    final saved = prefs.getString(_prefsKey);
    if (saved != null && _localizedValues.containsKey(saved)) {
      _currentLanguageCode = saved;
      notifyListeners();
    }
  }
